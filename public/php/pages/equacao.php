                <p class="entrada"><span class="space"></span>Em um mundo matematizado, há várias questões onde devemos descobrir "respostas ocultas" e vários outros fatores. Pensando nisso, temos as equações,
                um conjunto de fatores lógicos para determinar o valor ou os valores de determinada incógnita. Mas o que de fato é a equação? E o que é incógnita? Vamos ver nos próximos capítulos!
